package core.pojo;

public class UiObject{
    private String uiObjId = null;
    private String uiObjPage = null;
    private String uiObjType = null;
    private String uiObjName = null;
    private String uiObjPath = null;

    public void setUiObjId(String uiObjId){
        this.uiObjId=uiObjId;
    }
    public String getUiObjId(){
        return this.uiObjId;
    }

    public void setUiObjPage(String uiObjPage){
        this.uiObjPage=uiObjPage;
    }
    public String getUiObjPage(){
        return this.uiObjPage;
    }

    public void setUiObjType(String uiObjType){
        this.uiObjType=uiObjType;
    }
    public String getUiObjType(){
        return this.uiObjType;
    }

    public void setUiObjName(String uiObjName){
        this.uiObjName=uiObjName;
    }
    public String getUiObjName(){
        return this.uiObjName;
    }

    public void setUiObjPath(String uiObjPath){
        this.uiObjPath=uiObjPath;
    }
    public String getUiObjPath(){
        return this.uiObjPath;
    }
}